/// Rasterize a glyph into pixel data.
/// Returns the width, height, and pixel data (RGBA).
pub fn rasterize_glyph(c: char) -> Option<(u32, u32, Vec<u8>)> {
    let bits = get_font_bits(c)?;

    let width = 8u32;
    let height = 8u32;
    let mut pixels = vec![0u8; 64];

    for y in 0..8 {
        let row = bits[y];
        for x in 0..8 {
            if (row & (0x80 >> x)) != 0 {
                pixels[y * 8 + x] = 255;
            }
        }
    }

    Some((width, height, pixels))
}
